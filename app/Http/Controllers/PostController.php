    {
        //
        $postTitle = '';
        $postContent = '';
        if ($request->has('title') && $request->has('content')){
            $postTitle = $request->title;
            $postContent = $request->content;
        }

        $data = ['title' => $postTitle,'content' => $postContent];
        return response()->json($data);
    }

